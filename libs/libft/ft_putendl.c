#include "libft.h"

void	ft_putendl(char const *s)
{
	int		i;
	char	*ss;
	char	cc;

	if (!s)
		return ;
	cc = '\n';
	ss = (char *)s;
	i = 0;
	while (ss[i] != '\0')
	{
		write(1, &ss[i], 1);
		i++;
	}
	write(1, &cc, 1);
}
