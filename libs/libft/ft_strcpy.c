#include "libft.h"

char	*ft_strcpy(char *cp, const char *str)
{
	unsigned int		i;

	i = 0;
	while (str[i] != '\0')
	{
		cp[i] = str[i];
		i++;
	}
	cp[i] = '\0';
	return (cp);
}
